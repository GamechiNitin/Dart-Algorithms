void main() {
  var m1 = "I";
  var m2 = "Love";
  var m3 = "India";
  int a = 5;
  int b = 8;

  // Concatenation : using +
  print(m1 + " " + m2 + " " + m3);

  // Interpolation
  print("\nString Interpolation $m1 $m2 $m3");
  print("String Interpolation $m1 $m2 $m3".toUpperCase());
  print("${a * b}");
}
